// Sets the flag to 1 for any vertex that has an alpha of 255.
void set_vertex_flags(Vtx* verts, s32 count) {
    for (s32 i = 0; i < count; i++) {
        if (verts[i].v.cn[3] == 160) {
            verts[i].v.flag = 1;
        }
        else if (verts[i].v.cn[3] == 255) {
            verts[i].v.flag = 2;
        }
    }
}

// Sets the flags for every override vertex list.
void set_all_vertex_flags() {
    static bool flags_set = false;

    // Set the flags on all the vertices first if they haven't been set yet.
    if (!flags_set) {
        set_vertex_flags(time_override_verts, ARRAY_COUNT(time_override_verts));
        set_vertex_flags(epona_override_verts, ARRAY_COUNT(epona_override_verts));
        set_vertex_flags(saria_override_verts, ARRAY_COUNT(saria_override_verts));
        set_vertex_flags(scarecrow_override_verts, ARRAY_COUNT(scarecrow_override_verts));
        set_vertex_flags(oceff5_override_verts, ARRAY_COUNT(oceff5_override_verts));
        set_vertex_flags(oceff6_override_verts, ARRAY_COUNT(oceff6_override_verts));
        set_vertex_flags(healing_override_verts, ARRAY_COUNT(healing_override_verts));
        flags_set = true;
    }
}

// Patches the given DL to replace a vertex command at the given position with a branch to the new DL.
void patch_ocarina_effect(Actor* actor, Gfx* original_dl, u32 dl_offset, Gfx* override_dl) {
    set_all_vertex_flags();

    // Check if the DL hasn't been patched yet.
    if (original_dl[dl_offset].words.w0 >> 24 == G_VTX) {
        // Redirect the DL away from the original vertex command and to the override DL.
        gSPBranchList(&original_dl[dl_offset], override_dl);
    }
}

// Sets the alpha value for every vertex in the list with a set flag value.
void set_ocarina_vertex_alphas(Vtx* verts, s32 count, u8 alpha) {
    for (s32 i = 0; i < count; i++) {
        if (verts[i].v.flag) {
            verts[i].v.cn[3] = alpha;
        }
    }
}

